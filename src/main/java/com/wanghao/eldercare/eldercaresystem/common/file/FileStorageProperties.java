import com.wanghao.eldercare.eldercaresystem.dto.file.*;
import com.wanghao.eldercare.eldercaresystem.service.file.*;
import java.util.ArrayList;
import java.util.List;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

@Component
@ConfigurationProperties(prefix = "file")
public class FileStorageProperties {

    private String storageDir = "uploads";
    private long maxSizeBytes = 10L * 1024 * 1024;
    private List<String> allowedExtensions = new ArrayList<>(List.of("png", "jpg", "jpeg", "pdf"));

    public String getStorageDir() {
        return storageDir;
    }

    public void setStorageDir(String storageDir) {
        this.storageDir = storageDir;
    }

    public long getMaxSizeBytes() {
        return maxSizeBytes;
    }

    public void setMaxSizeBytes(long maxSizeBytes) {
        this.maxSizeBytes = maxSizeBytes;
    }

    public List<String> getAllowedExtensions() {
        return allowedExtensions;
    }

    public void setAllowedExtensions(List<String> allowedExtensions) {
        this.allowedExtensions = allowedExtensions;
    }
}

